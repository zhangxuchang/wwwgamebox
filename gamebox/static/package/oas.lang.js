/**
 * [getUrlParam description] 获取URL地址后面的参数
 * @param  {[string]} key  [description] 键值名称
 * @return {[type]}      [description]
 */
OASgetUrlParam = function(key){
	var val = null;
	var tempStr = window.location.search.substring(1);
	if(tempStr.length != 0){
		var arr = tempStr.split('&');
		var len = arr.length;
		for(i=0 ; i < len ; i++){
			if(arr[i].split('=')[0] == key){
				val = arr[i].split('=')[1];
				break;
			}
		}
	}
	return val;
};

/**
 * [OASWriteContent description] 动态写入多语言文件
 * @param {[string]} str [description]  键值名称
 */
OASWriteContent = function(key){
	var lang = OASgetUrlParam('lg');
	var str = LANG[lang][key];
	document.write(str);
}

/**
 * [OASGetLangVal description] 获取某个键值多语言信息
 * @param {[string]} key [description] 键值名称
 */
OASGetLangVal = function(key){
	var lang = OASgetUrlParam('lg');
	var str = LANG[lang][key];
	return str
}

